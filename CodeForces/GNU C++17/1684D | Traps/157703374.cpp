void test_case() {
   int N, K;
   cin >> N >> K;
 
   vector<int> A(N);
   for (auto &a : A)
      cin >> a;
 
   vector<int> order(N);
   iota (order.begin(), order.end(), 0);
   sort (order.begin(), order.end(), [&] (int i, int j) {
      return N - i -1 - A[i] < N - j - 1 - A[j];
   });
 
   vector<int> jump(N);
   for (int i = 0; i < K; ++i)
      jump[order[i]] = 1;
   
   int64_t ans = 0, cnt = 0;
   for (int i = 0; i < N; ++i) {
      if (jump[i])
         cnt += 1;
      else
         ans += A[i] + cnt;
   }
 
   cout << ans << '\n';
}
 
