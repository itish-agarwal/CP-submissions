void test_case() {
   int N, M;
   cin >> N >> M;
 
   if (N > M)
      swap (N, M);
 
   if (N == 1 && M > 2) {
      cout << -1 << '\n';
      return;
   }
 
   int ans = 2 * min (N - 1, M - 1);  
   M -= N;
 
   ans += 4 * (M / 2);
   if (M & 1)
      ans += 1;
 
   cout << ans << '\n';
}
 
