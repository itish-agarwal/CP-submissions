void test_case() {
  int N;
  cin >> N;

  vector<int> A(N);
  for(auto &a : A)
    cin >> a;

  vector<vector<int>> is_palin(N, vector<int> (N));

  for(int L = N - 1; L >= 0; --L) {
    for(int R = L; R < N; ++R) {
      if(L == R) {
        is_palin[L][R] = 1;
      }
      else {
        is_palin[L][R] = (A[L] == A[R] && (L + 1 <= R - 1 ? is_palin[L + 1][R - 1] : 1));
      }
    }
  }

  vector<int> dp(N, N);
  dp[N - 1] = 1;

  for(int i = N - 2; i >= 0; --i) {
    for(int j = i; j < N; ++j) {
      if(is_palin[i][j]) {
        dp[i] = min(dp[i], 1 + (j == N - 1 ? 0 : dp[j + 1]));
      }
    }
  }

  cout << dp[0] << '\n';
} 

