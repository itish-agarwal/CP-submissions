const long long inf = 2e15 + 9;
const int MAX_N = 1e6 + 7;
int N, A[MAX_N];
long long dp[MAX_N][5][5];

long long rec(int i, int took_first, int missed) {
  if(i == N - 1) {
    if(took_first) {
      return missed > 0 ? A[i] : 0;
    }
    else {
      return A[i];
    }
  }
  if(dp[i][took_first][missed] > -1)
    return dp[i][took_first][missed];

  long long res = A[i] + rec(i + 1, took_first || (i == 0), 0);

  if(missed == 0) {
    res = min(res, rec(i + 1, took_first, missed + 1));
  }

  return dp[i][took_first][missed] = res;
}

