void test_case() {
  int N;
  cin >> N;

  vector<long long> A(N);
  for(auto &a : A)
    cin >> a;

  //sorted
  long long ans = 0;

  for(int i = 0; i < N; ++i)
    for(int j = i + 2; j < N; ++j) {
      long long mid = (A[i] + A[j]) / 2;
      int inl = lower_bound(A.begin(), A.end(), mid) - A.begin();
      int inr = upper_bound(A.begin(), A.end(), mid) - A.begin() - 1;
      long long weight = 0;
      if(inl > i && inl < j)
        weight = max(weight, (A[i] - A[inl]) * (A[inl] - A[j]));

      if(inr > i && inr < j)
        weight = max(weight, (A[i] - A[inr]) * (A[inr] - A[j]));

      ans += weight;
    }

  cout << ans << '\n';
}

