const int nax = 1e5 + 7;

void test_case() {
  int N, Q;
  cin >> N >> Q;

  vector<int> cnt(nax);

  for(int i = 0; i < N; ++i) {
    int a;
    cin >> a;
    cnt[a] += 1;
  }

  for(int i = 1; i < nax; ++i)
    cnt[i] += cnt[i - 1];

  for(int q = 0; q < Q; ++q) {
    int L, R;
    cin >> L >> R;

    long long X = cnt[R] - (L > 0 ? cnt[L - 1] : 0);
    cout <<  10 * X - R + L << " ";
  }

  cout << '\n';
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int tests = 1;
  cin >> tests;

  while(tests--) 
    test_case();
}