signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while(tt--) {
    string s;
    cin >> s;
    int sum = 0;
    for(char ch : s) {
      sum += ch - '0';
    }
    if(sum % 3) {
      cout << "0\n";
    } else {
      cout << "1\n";
    }
  }
  return 0;
}
