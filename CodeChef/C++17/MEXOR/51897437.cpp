using ll = long long;
const ll INF = 2e15 + 9; 

ll fun(ll n) {
  ll res = 0;
  for(int bit = 0; bit < 34; ++bit) {
    ll mn = 1LL << bit;
    if(mn <= n)
      res += 1LL << bit;
  }

  return res;
}
void test_case() {
  ll X;
  cin >> X;

  ll lo = 0, hi = INF, mex = 0;
  while(lo <= hi) {
    ll mid = (lo + hi) / 2;
    ll OrUpto = fun(mid);
    if(OrUpto <= X) {
      lo = mid + 1;
      mex = mid;
    }
    else {
      hi = mid - 1;
    }
  } 

  cout << mex + 1 << '\n';
} 

