const int INF = 2e9 + 7;

int N;
string S;
string cpp = "c++";

int dp[100009][5];

int rec(int i, int j) {
  if(i == N) {
    return j == 3 ? INF : 0;
  }

  if(j == 3)
    return INF;

  if(dp[i][j] > -1)
    return dp[i][j];

  int res = 1 + rec(i + 1, j);
  res = min(res, rec(i + 1, j + (S[i] == cpp[j])));

  return dp[i][j] = res;
}

void test_case() {
  cin >> N >> S;

  memset(dp, -1, sizeof(dp));
  cout << rec(0, 0) << '\n';
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int tests = 1;
  cin >> tests;

  while(tests--) 
    test_case();
}