  cin >> N;
  vector<int> row(N);

  for(int i = 0; i < N; ++i) {
    int x, y;
    cin >> x >> y;
    --x, --y;
    row[y] = x;
  }

  int x = row[N - 1], y = N - 1;

  bool f1 = true, f2 = true;

  for(int i = N - 1; i >= row[N - 1]; --i, x++) 
    if(row[i] != x) {
      f1 = false;
      break;
    }

  x = row[0];
  for(int i = 0; i <= row[0]; ++i, x--)
    if(row[i] != x) {
      f2 = false;
      break;
    }

  debug(f1, f2);

