vector<pair<int, int>> get_factors(int n) {
   vector<pair<int, int>> res;
   for (int x = 2; x * x <= n; ++x) 
      if (n % x == 0) {
         int t = 0;
         while(n % x == 0) {
            n /= x;
            t++;
         }
         res.emplace_back(x, t);
      }

   if (n > 1)
      res.emplace_back(n, 1);

   return res;
}

void test_case() {
   int N;
   cin >> N;

   auto factors = get_factors(N);
   sort(factors.begin(), factors.end(), [&](const pair<int, int>& x, const pair<int, int>& y) {
      if (x.second != y.second)
         return x.second > y.second;

      return x.first < y.first;
   });

   // debug(factors);

   cout << factors[0].first << '\n';
}

